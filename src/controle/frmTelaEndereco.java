    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        String sql = "";
        try {
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o registro: ", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION, 3);
            if (resposta == 0) {
                sql = "DELETE FROM endereco_funcionarios WHERE sequencia=" + txtSeq.getText();
                int excluir = con_cliente.statement.executeUpdate(sql);
                if (excluir == 1) {
                    JOptionPane.showMessageDialog(null, "Exclusão realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                    con_cliente.executaSQL("select * from endereco_funcionarios order by sequencia");
                    con_cliente.resultset.first();
                    preencherTabela();
                    posicionarRegistro();
                } else {
                    JOptionPane.showMessageDialog(null, "Operação cancelada pelo usuário!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (SQLException excecao) {
            JOptionPane.showMessageDialog(null, "Erro na exclusão: " + excecao, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        String cod = txtCod.getText();
        String rua = txtRua.getText();
        String num = txtNum.getText();
        String cep = txtCep.getText();
        String bairro = txtBairro.getText();
        String cidade = txtCidade.getText();
        String uf = txtUf.getSelectedItem().toString();
        String pais = txtPais.getText();
        String sql = "";
        String msg = "";

        try {
            if (txtSeq.getText().equals("")) {
                sql = "insert into endereco_funcionarios(cod_funcionario, rua, numero, cep, bairro, cidade, uf, pais) values('" + cod + "','" + rua + "','"
                + num +"','"+ cep +"','"+ bairro +"','"+ cidade +"','"+ uf +"','"+ pais +"')";
                msg = "Gravação de um novo registro";
            } else {
                sql = "UPDATE endereco_funcionarios SET rua='" + rua + "', numero='" + num + "', cep='" + cep + "',"
                + " bairro='" + bairro + "', cidade='" + cidade + "', uf='" + uf + "', pais='" + pais + "' WHERE sequencia=" + txtSeq.getText();

                msg = "Alteração de registro";
            }
            con_cliente.statement.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, msg = " realizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);

            con_cliente.executaSQL("select * from endereco_funcionarios order by sequencia");
            con_cliente.resultset.first();
            mostrar_Dados();
            preencherTabela();

        } catch (SQLException errosql) {
            JOptionPane.showMessageDialog(null, "Erro na gravação: " + errosql, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

