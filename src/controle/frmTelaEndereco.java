    public void preencherTabela() {
        tblFuncionariosEnd.getColumnModel().getColumn(0).setPreferredWidth(11);
        tblFuncionariosEnd.getColumnModel().getColumn(1).setPreferredWidth(11);
        tblFuncionariosEnd.getColumnModel().getColumn(2).setPreferredWidth(100);
        tblFuncionariosEnd.getColumnModel().getColumn(3).setPreferredWidth(5);
        tblFuncionariosEnd.getColumnModel().getColumn(4).setPreferredWidth(10);
        tblFuncionariosEnd.getColumnModel().getColumn(5).setPreferredWidth(100);
        tblFuncionariosEnd.getColumnModel().getColumn(6).setPreferredWidth(100);
        tblFuncionariosEnd.getColumnModel().getColumn(7).setPreferredWidth(2);
        tblFuncionariosEnd.getColumnModel().getColumn(8).setPreferredWidth(100);
       
        DefaultTableModel modelo = (DefaultTableModel) tblFuncionariosEnd.getModel();
        modelo.setNumRows(0);

        try {
            con_cliente.resultset.beforeFirst();
            while (con_cliente.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_cliente.resultset.getString("sequencia"),
                    con_cliente.resultset.getString("cod_funcionario"),
                    con_cliente.resultset.getString("rua"),
                    con_cliente.resultset.getString("numero"),
                    con_cliente.resultset.getString("cep"),
                    con_cliente.resultset.getString("bairro"),
                    con_cliente.resultset.getString("cidade"),
                    con_cliente.resultset.getString("uf"),
                    con_cliente.resultset.getString("pais"),
                });
                
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! : \n" + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void posicionarRegistro() {
        try {
            con_cliente.resultset.first(); //posiciona no 1° registro da tabela
            mostrar_Dados(); //chama o método que irá buscar o dado da tabela
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Não foi possivel posicionar no primeiro registro:" + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void mostrar_Dados() {
        try {
            txtSeq.setText(con_cliente.resultset.getString("sequencia"));
            txtCod.setText(con_cliente.resultset.getString("cod_funcionario"));//Associar a caixa de texto ao campo cod
            txtRua.setText(con_cliente.resultset.getString("rua"));
            txtNum.setText(con_cliente.resultset.getString("numero")); // Convertendo String para Integer
            txtCep.setText(con_cliente.resultset.getString("cep"));
            txtBairro.setText(con_cliente.resultset.getString("bairro"));
            txtCidade.setText(con_cliente.resultset.getString("cidade"));
            txtUf.setSelectedItem(con_cliente.resultset.getString("uf"));
            txtPais.setText(con_cliente.resultset.getString("pais"));
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Não localizou dados: " + erro, "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

